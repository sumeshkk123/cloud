function getTranslation(locale, path) {
  const localeData = translations[locale] || {};
  const defaultData = translations.en;
  const value =
    path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), localeData) ??
    path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), defaultData);
  return value;
}

